package com.class01;

public class DataTypes {

	public static void main(String[] args) {
		// Family to store whole numbers
		// datatype variable name = assigning value
		byte b = 10;
		byte b1 = -128;
		// byte b2=128; compiler will give an error

		short s = 6566;
		short s1 = -32768;
		// short s3=32768; compiler will give an error

		int i = 767787;
		// int i2= true;

		long l = 5765765757576l;
		long l1 = 76878787555875L;

		// family to hold numbers with floating points

		float f = 12.78f;
		double d = 14.767876689876765688980909;

		// boolean datatype

		boolean hungry = true;
		boolean rain = false;

		// to store single character

		char container = 'A';

		System.out.println(hungry);
		System.out.println(b1);
		System.out.println(container);
	}
}