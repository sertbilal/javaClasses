package com.class1;

public class PractiveVariables {
	public static void main(String[] args) {
		byte b = 11;
		byte b1 = -120;

		short s = 2222;
		short s1 = -32768;

		int i = 565565;

		long l = 1234512345;
		long l1 = 76878787555875L;

		float f = 25.25f;
		double d = 25.2525252525252525252;

		boolean islam = true;
		boolean terror = false;

		char container = '$';
		// single character kullan burada

		System.out.println(container);
		System.out.println(s);
		System.out.println(s1);
		System.out.println(i);
		System.out.println(l);
		System.out.println(l1);
		System.out.println(f);
		System.out.println(d);
		System.out.println(islam);
		System.out.println(terror);

	}

}
